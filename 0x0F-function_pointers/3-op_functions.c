#include "3-calc.h"

/**
 * op-add - adds two ints.
 * @a: int 1.
 * @b: int 2.
 * Return: sum.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op-sub - subtracts two ints.
 * @a: int 1.
 * @b: int 2.
 * Return: difference.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op-mul - multiplies two ints.
 * @a: int 1.
 * @b: int 2.
 * Return: product.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op-div - divides two ints.
 * @a: int 1.
 * @b: int 2.
 * Return:quotient.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op-mod - modulo two ints.
 * @a: int 1.
 * @b: int 2.
 * Return: div remainder.
 */
int op_mod(int a, int b)
{
	return (a % b);
}



